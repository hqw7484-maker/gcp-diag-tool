#!/bin/bash
# ==========================================
# GCS 专用：高颜值心跳保活仪表盘
# ==========================================

# 清屏，给仪表盘留空间
clear
echo -e "\033[36m"
echo "################################################"
echo "#          GCS 终端守卫 - 持续运行中           #"
echo "################################################"
echo -e "\033[0m"

# 获取当前公网链接（如果有）
LINK=$(grep -o 'https://[-0-9a-z]*\.trycloudflare.com' cf.log 2>/dev/null | head -n 1)
[ -n "$LINK" ] && echo -e "🔗 \033[1m当前节点:\033[0m \033[32m$LINK\033[0m" || echo -e "🔗 \033[1m当前节点:\033[0m \033[31m未检测到链接 (请确认服务已启动)\033[0m"
echo -e "🕒 \033[1m启动时间:\033[0m $(date '+%Y-%m-%d %H:%M:%S')"
echo -e "------------------------------------------------"
echo ""

# 核心监控循环 (540分钟 = 9小时)
for i in {1..540}
do
    # 计算进度条
    PERCENT=$(( i * 100 / 540 ))
    BAR=$(printf "%0.s#" $(seq 1 $((PERCENT / 5))))
    SPACES=$(printf "%0.s " $(seq 1 $((20 - PERCENT / 5))))

    # 原地刷新输出 (使用了 \r)
    printf "\r\033[35m[Keep-Alive]\033[0m 进度: [%-20s] %d%% | 运行: %d/540min | 心跳: %s " "$BAR" "$PERCENT" "$i" "$(date '+%H:%M:%S')"
    
    # 每 5 分钟产生一个换行日志，防止某些环境检测死循环
    if [ $((i%5)) -eq 0 ]; then
        echo "" 
    fi
    
    sleep 60
done

echo -e "\n\033[31m[System] 9小时任务已到期，建议重启以维持稳定。\033[0m"
